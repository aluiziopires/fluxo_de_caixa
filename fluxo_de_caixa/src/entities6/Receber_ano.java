package entities6;

public class Receber_ano {

	private String mesR;
	private double valorRecebidoAno;
	
	public Receber_ano(String mesR, double valorRecebidoAno) {
		this.mesR = mesR;
		this.valorRecebidoAno = valorRecebidoAno;
	}

	public String getMesR() {
		return mesR;
	}

	public void setMesR(String mesR) {
		this.mesR = mesR;
	}

	public double getValorRecebidoAno() {
		return valorRecebidoAno;
	}

	public void setValorRecebidoAno(double valorRecebidoAno) {
		this.valorRecebidoAno = valorRecebidoAno;
	}
	
	
	
}
