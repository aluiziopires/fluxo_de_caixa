package Entities4;

public class Receber_mes {

	private String dataR;
	private double valorRecebidoMes;
	
	public Receber_mes(String dataR, double valorRecebidoMes) {
		this.dataR = dataR;
		this.valorRecebidoMes = valorRecebidoMes;
	}

	public String getDataR() {
		return dataR;
	}

	public void setDataR(String dataR) {
		this.dataR = dataR;
	}

	public double getValorRecebidoMes() {
		return valorRecebidoMes;
	}

	public void setValorRecebidoMes(double valorRecebidoMes) {
		this.valorRecebidoMes = valorRecebidoMes;
	}
	
	
	
}
